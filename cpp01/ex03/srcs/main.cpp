#include "HumanA.hpp"
#include "HumanB.hpp"
#include "Weapon.hpp"

/*
** If object can be NULL or inexisting (like weapons here) it's appropriate to
**	use pointer.
**
** If object can't be NULL and must always exist (like name here) it's appropriate to
** use reference.
*/

int		main(void)
{
	{
		Weapon 	club = Weapon("crude spiked club");
		
		HumanA bob("Bob", club);
		bob.attack();
		club.setType("some other type of club");
		bob.attack();
	}
	{
		Weapon	club = Weapon("crude spiked club");
		HumanB jim("Jim");
		jim.setWeapon(club);
		jim.attack();
		club.setType("some other type of club");
		jim.attack();
	}
}
